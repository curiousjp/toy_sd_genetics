class SizeChromo(Chromosome):
    known_alleles = {
        'large_width',
        'large_height',
    }

    def express(self, perturbation=0.0):
        expressed_genes = Chromosome.express(self, perturbation)
        if 'large_width' and 'large_height' in expressed_genes: return {'meta_bigsquare'}
        elif 'large_width' in expressed_genes: return {'meta_landscape'}
        elif 'large_height' in expressed_genes: return {'meta_portrait'}
        else: return {'meta_square'}


