import random

# this is really an abstraction of chromsome pairs,

# generalised to n instead of the usual two

class Chromosome:
    # Shared class variable representing the names of the alleles
    known_alleles = set()

    def __init__(self, *gene_sets):
        # Initialize the sets for parent-contributed genes
        self.genes = [set() for _ in range(len(gene_sets))]
        
        # Filter and store genes
        for i, gene_set in enumerate(gene_sets):
            self.genes[i] = set(gene for gene in gene_set if gene in self.known_alleles)

    def mutate(self, probability):
        had_mutation = False
        for gene_set in self.genes:
            for allele in self.known_alleles:
                if random.random() < probability:
                    had_mutation = True
                    # Flip the presence or absence of the allele
                    if allele in gene_set:
                        gene_set.remove(allele)
                    else:
                        gene_set.add(allele)
        return had_mutation

    def express(self, perturbation=0.0):
        expressed_genes = set()
        for allele in self.known_alleles:
            presence = sum(1 for gene_set in self.genes if allele in gene_set)
            likelihood = presence / len(self.genes)

            if perturbation > 0:
                likelihood += random.gauss(0, perturbation)
                likelihood = min(max(likelihood, 0), 1)  # Ensuring within [0,1]

            if random.random() < likelihood:
                expressed_genes.add(allele)

        return expressed_genes
    
